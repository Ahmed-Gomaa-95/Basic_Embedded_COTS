#include "STD_TYPES.h"
#include "BIT_MATH.h"
#include "GIE_reg.h"
#include "GIE_interface.h"

/**********************************************************************************************************************/
/**********************************************************************************************************************/
/*****                                                                                                            *****/
/***** @Function Name 		  : GIE_voidEnable                                                                    *****/
/***** @Function Description  : function enable global interrupt					                              *****/
/***** @Function Parameters   : void                                                                              *****/
/***** @Function Return       :  void																	          *****/
/*****                                                                                                            *****/
/**********************************************************************************************************************/
/**********************************************************************************************************************/
void GIE_voidEnable(void)
{
	/* Set I bit to enable global interrupt */
	SET_BIT(SREG,SREG_I);


}
/**********************************************************************************************************************/
/**********************************************************************************************************************/
/*****                                                                                                            *****/
/***** @Function Name 		  : GIE_voidDisable                                                                   *****/
/***** @Function Description  : function disable global interrupt					                              *****/
/***** @Function Parameters   : void                                                                              *****/
/***** @Function Return       : void																	          *****/
/*****                                                                                                            *****/
/**********************************************************************************************************************/
/**********************************************************************************************************************/
void GIE_voidDisable(void)
{
	/* clear I bit to disable global interrupt */
	CLR_BIT(SREG,SREG_I);

}
