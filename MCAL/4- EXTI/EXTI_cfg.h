#ifndef EXTI_CFG_H_
#define EXTI_CFG_H_




/************************************************************************************/
/*	To trigger interrupt 0 and 1 you can choose from this options 				*****/
 /* 	if you want to trigger interrupt 2 choose from first 2 options only  : 	*****/
/*  													1- RISING_EDGE			*****/
/* 														2- FALLING_EDGE			*****/
/* 														3- ON_CHANGE			*****/
/* 														4- LOW_LEVEL 			*****/
/* **********************************************************************************/
#define EXTI_u8INT0_TRIG		FALLING_EDGE
#define EXTI_u8INT1_TRIG		FALLING_EDGE
#define EXTI_u8INT2_TRIG		FALLING_EDGE

/************************************************************************************/
/*	To Enable or Disable any of interrupt sources you can choose from options: 	*****/
/*  													1- ENABLED   			*****/
/* 														2- DISABLED	    		*****/
/* **********************************************************************************/

#define EXTI_u8INT0_STATUS		ENABLED
#define EXTI_u8INT1_STATUS		ENABLED
#define EXTI_u8INT2_STATUS		ENABLED


#endif /* EXTI_CFG_H_ */
